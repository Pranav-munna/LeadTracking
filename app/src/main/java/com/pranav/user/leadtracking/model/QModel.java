package com.pranav.user.leadtracking.model;

public class QModel {

    String a,l,k,j,h;

    public QModel(String a, String l, String k, String j, String h) {
        this.a = a;
        this.l = l;
        this.k = k;
        this.j = j;
        this.h = h;
    }

    public String getA() {

        return a;
    }

    public void setA(String a) {
        this.a = a;
    }

    public String getL() {
        return l;
    }

    public void setL(String l) {
        this.l = l;
    }

    public String getK() {
        return k;
    }

    public void setK(String k) {
        this.k = k;
    }

    public String getJ() {
        return j;
    }

    public void setJ(String j) {
        this.j = j;
    }

    public String getH() {
        return h;
    }

    public void setH(String h) {
        this.h = h;
    }
}
